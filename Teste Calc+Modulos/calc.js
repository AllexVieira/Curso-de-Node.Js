// Calculadora básica com números fixos e com funções em um único .Js
function soma(a,b) {
    return a+b
}

function sub(a,b) {
    return a-b
}

function multi(a,b) {
    return a*b
}

function div(a,b) {
    return a/b
}

console.log(soma(2,4))
console.log(sub(2,4))
console.log(multi(2,4))
console.log(div(2,4))

console.log("A soma é "+soma(2,4)+", a subtração é "+sub(2,4)+", a multiplicação é "+multi(2,4)+", e a divisão é "+div(2,4)+".")